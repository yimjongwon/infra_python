nums = [10, 20, 30, 40, 50]

'''
    Slice의 기본 문법
    [시작 : 끝 : 증감]
'''

print(nums[1:])
print(nums[2:])
print(nums[3:])

print("-----------")

print(nums[:3])

print("-----------")
# 20,30,40이 들어있는 list를 얻어내려면?
print(nums[1:4])

print("-----------")
print(nums[::1])
print(nums[::2])
print(nums[::3])
